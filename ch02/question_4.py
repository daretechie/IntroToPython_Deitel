# TODO: 4. (Arithmetic) For each of the arithmetic operators +, -, *, /, // and display the value of an
#  expression with 27.5 as the left operand and2 as the right operand.


x, y = 27.5, 2

print(x, '+', y, '=', x+y)
print(x, '-', y, '=', x-y)
print(x, '*', y, '=', x*y)
print(x, '/', y, '=', x/y)
print(x, '//', y, '=', x//y)
print(x, '**', y, '=', x**y)
